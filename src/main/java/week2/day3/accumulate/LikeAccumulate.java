package week2.day3.accumulate;

public class LikeAccumulate {
    public static void main(String[] args) {
        int likeCount = 0;

        likeCount = likeCount + 1;  // 좋아요 한 개 받음
        System.out.println(likeCount);

        likeCount = likeCount + 1;  // 좋아요 한 개 더 받음
        System.out.println(likeCount);

        likeCount = likeCount + 1;  // 좋아요 한 개 더 받음
        System.out.println(likeCount);
    }
}
